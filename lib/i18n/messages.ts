function replaceParams(text: string, params: Record<string, string | number>): string {
  return Object.entries(params).reduce(
    (acc, [key, value]) => acc.replace(new RegExp(`\\{${key}\\}`, "g"), String(value)),
    text
  );
}

export function getMessage(locale: Locale, key: string, params?: Record<string, string | number>): string {
  const msg = messages[locale][key] ?? messages.en[key] ?? key;
  return params ? replaceParams(msg, params) : msg;
}
